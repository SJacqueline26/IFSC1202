x = 1    # x is type integer
print(x)
print(type(x))

y = 2.8  # y is type float
print (y)
print (type(y))

# Integer is a whole number, positive or negative, 
# without decimals, of unlimited length.
x = 1
y = 35656222554887711
z = -3255522

print(x)
print(type(x))
print(y)
print(type(y))
print(z)
print(type(z))

# Float in number, positive or negative, containing a decimal. 
x = 1.10
y = 1.0
z = -35.59

print(x)
print(type(x))
print(y)
print(type(y))
print(z)
print(type(z))

# Float can also be scientific numbers with an "e" 
# to indicate the power of 10.
x = 35e3
y = 12E4
z = -87.7e100

print(x)
print(type(x))
print(y)
print(type(y))
print(z)
print(type(z))